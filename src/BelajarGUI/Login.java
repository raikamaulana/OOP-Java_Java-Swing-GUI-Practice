    private void butLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butLoginActionPerformed

        String usn = textFUsername1.getText();
        String password = String.valueOf(jPasswordField.getPassword());
        if (usn.equals("Masukkan username") && password.equals("Masukkan password")) {
            JOptionPane.showMessageDialog(null, "<html><b>Username</b> dan <b>Password</b> kosong</html>", "✿ PESAN ✿", JOptionPane.WARNING_MESSAGE, iconGagal);
        } else if (usn.equals("Masukkan username") && !(password.equals("Masukkan password"))) {
            JOptionPane.showMessageDialog(null, "<html><b>Username</b> kosong</html>", "✿ PESAN ✿", JOptionPane.WARNING_MESSAGE, iconGagal);
        } else if (password.equals("Masukkan password") && !(usn.equals("Masukkan username"))) {
            JOptionPane.showMessageDialog(null, "<html><b>Password</b> kosong</html>", "✿ PESAN ✿", JOptionPane.WARNING_MESSAGE, iconGagal);
        } else {
            try {
                if ((!usn.equals("")) && 
                        (String.valueOf(jPasswordField.getPassword()).equals("yuuki"))) {
                    JOptionPane.showMessageDialog(null, "<html>Selamat Datang <b>'" + usn + "'</b></html>", "✿ PESAN ✿", JOptionPane.INFORMATION_MESSAGE, iconSukses);
                    ses.setUsername(usn);
                    new MenuUtama().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "<html><b>Username</b> atau <b>Password</b> salah</html>", "✿ PESAN ✿", JOptionPane.WARNING_MESSAGE, iconGagal);
                    jPasswordField.setText("");
                    placeholder(textFUsername1);
                    placeholderPass();
                    blueBorder(textFUsername1);
                    blueBorder(jPasswordField);
                    placeholder(jPasswordField);
                    textFUsername1.setText("Masukkan username");
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, "Error", "✿ PESAN ✿", JOptionPane.WARNING_MESSAGE, iconGagal);
            }
        }
    }//GEN-LAST:event_butLoginActionPerformed

